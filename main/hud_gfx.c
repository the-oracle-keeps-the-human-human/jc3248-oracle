/* ── Pixel ──────────────────────────────────────────────────────────────────*/
void gfx_pixel(uint16_t *fb, int fb_w, int fb_h, int x, int y, uint16_t color)
{
    if (!fb) return;
    if (x < 0 || y < 0 || x >= fb_w || y >= fb_h) return;
    /* The esp_lcd path pushes the framebuffer big-endian (same reason the GIF
     * decoder uses BIG_ENDIAN_PIXELS). Store byte-swapped so host-order colours
     * like GFX_GREEN (0x07E0) render correctly — without this, green showed RED. */
    fb[(size_t)y * fb_w + x] = __builtin_bswap16(color);
}

/* ── Filled rect (clipped span fill — faster than per-pixel) ────────────────*/
void gfx_fill_rect(uint16_t *fb, int fb_w, int fb_h,
                   int x, int y, int w, int h, uint16_t color)
{
    if (!fb || w <= 0 || h <= 0) return;
    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = x + w; if (x1 > fb_w) x1 = fb_w;
    int y1 = y + h; if (y1 > fb_h) y1 = fb_h;
    if (x0 >= x1 || y0 >= y1) return;
    for (int yy = y0; yy < y1; yy++) {
        uint16_t *row = &fb[(size_t)yy * fb_w];
        for (int xx = x0; xx < x1; xx++) row[xx] = __builtin_bswap16(color);  /* big-endian fb (see gfx_pixel) */
    }
}

/* ── Fast lines ─────────────────────────────────────────────────────────────*/
void gfx_hline(uint16_t *fb, int fb_w, int fb_h, int x, int y, int w, uint16_t color)
{
    gfx_fill_rect(fb, fb_w, fb_h, x, y, w, 1, color);
}
void gfx_vline(uint16_t *fb, int fb_w, int fb_h, int x, int y, int h, uint16_t color)
{
    gfx_fill_rect(fb, fb_w, fb_h, x, y, 1, h, color);
}

/* ── Rect outline ───────────────────────────────────────────────────────────*/
void gfx_rect(uint16_t *fb, int fb_w, int fb_h,
              int x, int y, int w, int h, uint16_t color)
{
    if (w <= 0 || h <= 0) return;
    gfx_hline(fb, fb_w, fb_h, x, y, w, color);             /* top    */
    gfx_hline(fb, fb_w, fb_h, x, y + h - 1, w, color);     /* bottom */
    gfx_vline(fb, fb_w, fb_h, x, y, h, color);             /* left   */
    gfx_vline(fb, fb_w, fb_h, x + w - 1, y, h, color);     /* right  */
}

/* ── Filled circle (integer midpoint, horizontal-span fill) ─────────────────*/
void gfx_fill_circle(uint16_t *fb, int fb_w, int fb_h,
                     int cx, int cy, int r, uint16_t color)
{
    if (r < 0) return;
    if (r == 0) { gfx_pixel(fb, fb_w, fb_h, cx, cy, color); return; }
    /* For each scanline within radius, fill the chord [cx-dx, cx+dx]. Using
     * dx = round(sqrt(r^2 - dy^2)) via integer compare keeps it dependency-free
     * (no <math.h>) and gives a clean disc for the small dot radii we use. */
    int r2 = r * r;
    for (int dy = -r; dy <= r; dy++) {
        int dy2 = dy * dy;
        int dx = 0;
        while ((dx + 1) * (dx + 1) + dy2 <= r2) dx++;
        gfx_hline(fb, fb_w, fb_h, cx - dx, cy + dy, 2 * dx + 1, color);
    }
}

/* ── One glyph ──────────────────────────────────────────────────────────────*/
int gfx_char(uint16_t *fb, int fb_w, int fb_h,
             int x, int y, char ch, int scale, uint16_t fg, uint32_t bg)
{
    if (scale < 1) scale = 1;
    int adv = GFX_FONT_W * scale;

    unsigned c = (unsigned char)ch;
    if (c < 0x20 || c > 0x7E) c = 0x20;      /* non-printable -> space */
    const uint8_t *glyph = font8x8[c - 0x20];

    bool opaque = (bg != GFX_TRANSPARENT);
    uint16_t bg16 = (uint16_t)(bg & 0xFFFF);

    /* Fill the cell first when opaque (one rect, cheap + clipped). */
    if (opaque) gfx_fill_rect(fb, fb_w, fb_h, x, y, adv, GFX_FONT_H * scale, bg16);

    for (int row = 0; row < GFX_FONT_H; row++) {
        uint8_t bits = glyph[row];
        if (!bits) continue;                  /* blank row — nothing lit */
        int py = y + row * scale;
        for (int col = 0; col < GFX_FONT_W; col++) {
            if (!(bits & (1u << col))) continue;   /* bit0 = leftmost column */
            int px = x + col * scale;
            /* one scaled block per lit font pixel */
            gfx_fill_rect(fb, fb_w, fb_h, px, py, scale, scale, fg);
        }
    }
    return adv;
}

/* ── String ─────────────────────────────────────────────────────────────────*/
int gfx_text(uint16_t *fb, int fb_w, int fb_h,
             int x, int y, const char *s, int scale, uint16_t fg, uint32_t bg)
{
    if (!s) return x;
    if (scale < 1) scale = 1;
    int adv = GFX_FONT_W * scale;
    int cx = x, cy = y;
    for (const char *p = s; *p; p++) {
        if (*p == '\n') { cx = x; cy += GFX_FONT_H * scale; continue; }
        gfx_char(fb, fb_w, fb_h, cx, cy, *p, scale, fg, bg);
        cx += adv;
    }
    return cx;
}

/* ── Measure ────────────────────────────────────────────────────────────────*/
int gfx_text_width(const char *s, int scale)
{
    if (!s) return 0;
    if (scale < 1) scale = 1;
    int adv = GFX_FONT_W * scale;
    int run = 0, best = 0;
    for (const char *p = s; *p; p++) {
        if (*p == '\n') { if (run > best) best = run; run = 0; continue; }
        run += adv;
    }
    if (run > best) best = run;
    return best;
}
